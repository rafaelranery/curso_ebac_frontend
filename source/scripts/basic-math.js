function add(numberA, numberB) {
    return numberA + numberB
}

function subtract(numberA, numberB) {
    return numberA - numberB
}

function divide(numberA, numberB) {
    return numberA / numberB
}

function multiply(numberA, numberB) {
    return numberA * numberB
}

function sqr(numberA) {
    return numberA * numberA
}